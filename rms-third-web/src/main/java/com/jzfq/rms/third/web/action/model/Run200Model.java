package com.jzfq.rms.third.web.action.model;

public class Run200Model {

    private String phone;//电话号
    private String name;//姓名
    private String idCard;//身份证号
    private String tdScore;//同盾分
    private String brScore;//百融分
    private String three;//三要素
    private String length;//在网时长
    private String status;//在网状态


    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIdCard() {
        return idCard;
    }

    public void setIdCard(String idCard) {
        this.idCard = idCard;
    }

    public String getTdScore() {
        return tdScore;
    }

    public void setTdScore(String tdScore) {
        this.tdScore = tdScore;
    }

    public String getBrScore() {
        return brScore;
    }

    public void setBrScore(String brScore) {
        this.brScore = brScore;
    }

    public String getThree() {
        return three;
    }

    public void setThree(String three) {
        this.three = three;
    }

    public String getLength() {
        return length;
    }

    public void setLength(String length) {
        this.length = length;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
